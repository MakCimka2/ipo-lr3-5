num = float(input ("Введите число")) # Зарос числа
if num > 0: # Если число больше 0
   print("Положительное") # Выводит Положительное
elif num == 0: # Если число равно 0
   print ("Ни положительное, ни отрицательное") # Выводит Ни положительное, ни отрицательное
else: 
   print("Отрицательное") #Выводит Отрицательное